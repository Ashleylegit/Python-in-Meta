# Local scope
#Local scope refers to a variable declared inside a function. 
#For example, in the code below, the variable total is only 
#available to the code within the get_total function. 
# Anything outside of this function will not have access to it.


#Enclosing scope refers to a function inside another function or what
# is commonly called a nested function. 


#Global scope is when a variable is declared outside of a function. 
#This means it can be accessed from anywhere. 


#Built-in scope refers to the reserved keywords that 
# Python uses for its built-in functions, such as print, def, for, in, and so forth.  Functions with built-in scope can be accessed at any level.

